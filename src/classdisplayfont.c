struct ClassDisplayFont * GetClassDisplayFontInfo(char chr) {

    if ((u8)(chr - 0x61) < 0x1A) {
        return &gClassDisplayFontData[chr-0x47];
    }

    if ((u8)(chr - 0x41) < 0x1A) {
        return &gClassDisplayFontData[chr-0x41];
    }

    if (chr == 0x2E) {
        return &gClassDisplayFontData[0x34];
    }

    return NULL;
}
